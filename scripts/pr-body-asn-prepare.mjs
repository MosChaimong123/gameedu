import { execSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

function parseArgs(argv) {
  const args = {
    source: "docs/assignment-command-center-pr-body-ready-asn-214.md",
    outDir: "tmp/pr-bodies",
    outFile: "",
  };

  for (let i = 2; i < argv.length; i += 1) {
    const part = argv[i];
    if (part === "--source" && argv[i + 1]) {
      args.source = argv[i + 1];
      i += 1;
      continue;
    }
    if (part === "--out-dir" && argv[i + 1]) {
      args.outDir = argv[i + 1];
      i += 1;
      continue;
    }
    if (part === "--out-file" && argv[i + 1]) {
      args.outFile = argv[i + 1];
      i += 1;
      continue;
    }
    if (part === "--help" || part === "-h") {
      printHelpAndExit(0);
    }
    throw new Error(`Unknown arg: ${part}`);
  }

  return args;
}

function printHelpAndExit(code) {
  const lines = [
    "Prepare ASN PR body from template.",
    "",
    "Usage:",
    "  node scripts/pr-body-asn-prepare.mjs [--source <path>] [--out-dir <path>] [--out-file <path>]",
    "",
    "Defaults:",
    "  --source   docs/assignment-command-center-pr-body-ready-asn-214.md",
    "  --out-dir  tmp/pr-bodies",
    "",
    "Examples:",
    "  npm run pr:body:asn",
    "  node scripts/pr-body-asn-prepare.mjs --source docs/assignment-command-center-pr-body-template.md",
    "  node scripts/pr-body-asn-prepare.mjs --out-file tmp/pr-bodies/custom.md",
  ];
  console.log(lines.join("\n"));
  process.exit(code);
}

function safeBranchName(raw) {
  return raw.replace(/[^\w.-]+/g, "-");
}

function getCurrentBranch() {
  try {
    const out = execSync("git rev-parse --abbrev-ref HEAD", { encoding: "utf8" });
    return out.trim();
  } catch {
    return "unknown-branch";
  }
}

function makeTimestamp() {
  const d = new Date();
  const pad = (n) => String(n).padStart(2, "0");
  return [
    d.getUTCFullYear(),
    pad(d.getUTCMonth() + 1),
    pad(d.getUTCDate()),
    "-",
    pad(d.getUTCHours()),
    pad(d.getUTCMinutes()),
    pad(d.getUTCSeconds()),
    "Z",
  ].join("");
}

function main() {
  const args = parseArgs(process.argv);
  const cwd = process.cwd();

  const sourcePath = path.resolve(cwd, args.source);
  const source = readFileSync(sourcePath, "utf8");

  const branch = getCurrentBranch();
  const branchSafe = safeBranchName(branch);
  const stamp = makeTimestamp();

  const metadata = [
    "<!-- Generated by scripts/pr-body-asn-prepare.mjs -->",
    `<!-- branch: ${branch} -->`,
    `<!-- generated_at_utc: ${new Date().toISOString()} -->`,
    "",
  ].join("\n");

  const outputBody = `${metadata}${source}`;

  const outPath = args.outFile
    ? path.resolve(cwd, args.outFile)
    : path.resolve(cwd, args.outDir, `asn-pr-body-${branchSafe}-${stamp}.md`);

  mkdirSync(path.dirname(outPath), { recursive: true });
  writeFileSync(outPath, outputBody, "utf8");

  console.log(`Prepared PR body: ${path.relative(cwd, outPath).replace(/\\/g, "/")}`);
  console.log(
    `Use with: gh pr create --title "<PR title>" --body-file "${path
      .relative(cwd, outPath)
      .replace(/\\/g, "/")}"`
  );
}

main();
